#!/usr/bin/env node
/* Nik — inventory generator.
 *
 * Walks web/src/{contracts,lib,components,screens,native,theme,types}/**
 * and emits docs/Inventory.generated.md — a flat, machine-checkable
 * register of every export. Regenerate any time the file structure
 * changes; commit the result so it shows up in PR diffs.
 *
 * Heuristic-based (regex over file contents — no TS AST). Trades
 * perfect accuracy for zero deps. Catches the common cases:
 *   export const FOO = ...
 *   export function foo()
 *   export class Foo
 *   export type Foo = ...
 *   export default function Foo()
 *   export { foo, bar }
 */

import { readdirSync, readFileSync, writeFileSync, statSync } from 'node:fs';
import { join, dirname, relative } from 'node:path';
import { fileURLToPath } from 'node:url';

const here = dirname(fileURLToPath(import.meta.url));
const repoRoot = join(here, '..');
const webSrc = join(repoRoot, 'web', 'src');
const outPath = join(repoRoot, 'docs', 'Inventory.generated.md');

const SECTIONS = [
  { id: 'contracts', dir: 'contracts', title: 'Contracts (the registry)', desc: 'Backend ops + UI commands. The MCP server auto-exposes these.' },
  { id: 'lib',       dir: 'lib',       title: 'Library (hooks + helpers)', desc: 'React hooks, helpers, the LLM router, the Supabase client.' },
  { id: 'components',dir: 'components',title: 'Components',                desc: 'Primitives, shell, sheets, icons.' },
  { id: 'screens',   dir: 'screens',   title: 'Screens',                   desc: 'Top-level routes. Each pair: <Name>Screen.tsx + <Name>Screen.manifest.ts.' },
  { id: 'native',    dir: 'native',    title: 'Native bridge (Capacitor)', desc: 'Status bar, preferences, geolocation, etc.' },
  { id: 'theme',     dir: 'theme',     title: 'Theme + score',             desc: 'Theme universes + score model.' },
  { id: 'types',     dir: 'types',     title: 'Types',                     desc: 'Shared type-only modules.' },
  { id: 'data',      dir: 'data',      title: 'Mock data',                 desc: 'Locale-neutral seed shapes used by un-migrated screens.' },
];

const exportRE = [
  // export const NAME =
  /^export\s+(?:const|let|var)\s+(\w+)/,
  // export function NAME(
  /^export\s+(?:async\s+)?function\s+(\w+)/,
  // export class NAME
  /^export\s+class\s+(\w+)/,
  // export type NAME =   |   export interface NAME
  /^export\s+(?:type|interface)\s+(\w+)/,
  // export default function NAME(
  /^export\s+default\s+function\s+(\w+)/,
  // export default class NAME
  /^export\s+default\s+class\s+(\w+)/,
  // export { foo, bar }
  /^export\s+\{\s*([^}]+?)\s*\}/,
];

function walk(dir) {
  const out = [];
  let entries;
  try { entries = readdirSync(dir); } catch { return out; }
  for (const name of entries.sort()) {
    const full = join(dir, name);
    const st = statSync(full);
    if (st.isDirectory()) out.push(...walk(full));
    else if (/\.(ts|tsx)$/.test(name)) out.push(full);
  }
  return out;
}

function extractExports(src) {
  const lines = src.split(/\r?\n/);
  const exports = new Set();
  let hasDefault = false;
  for (const line of lines) {
    for (const re of exportRE) {
      const m = re.exec(line);
      if (!m) continue;
      const captured = m[1];
      // export { foo, bar as baz, type Q }
      if (captured.includes(',') || captured.includes(' as ') || captured.startsWith('type ')) {
        const names = captured.split(',').map((s) =>
          s.trim().replace(/^type\s+/, '').split(/\s+as\s+/).pop().trim(),
        ).filter(Boolean);
        for (const n of names) exports.add(n);
      } else {
        exports.add(captured);
      }
      if (line.includes('export default')) hasDefault = true;
    }
  }
  return { exports: [...exports].sort(), hasDefault };
}

function describe(src) {
  // First /* */ or // comment in the file = description.
  const block = /\/\*\s*\n?(.*?)\*\//s.exec(src);
  if (block) {
    return block[1].split(/\r?\n/).map((s) => s.replace(/^\s*\*?\s?/, '').trim())
      .filter((l) => l && !l.startsWith('Nik —')).slice(0, 1).join(' ').slice(0, 140);
  }
  const line = /^\/\/\s*(.+)$/m.exec(src);
  return line ? line[1].slice(0, 140) : '';
}

let md = `# Inventory (generated)

> **Auto-generated by [\`scripts/build-inventory.mjs\`](../scripts/build-inventory.mjs).** Do not edit by hand. Run \`node scripts/build-inventory.mjs\` to regenerate after structural changes. The hand-curated [\`INVENTORY.md\`](../INVENTORY.md) at repo root has the human-readable narrative; this file is the machine-readable diff target for PR review.

`;

let totalFiles = 0, totalExports = 0;

for (const section of SECTIONS) {
  const dir = join(webSrc, section.dir);
  const files = walk(dir);
  if (!files.length) continue;
  md += `## ${section.title}\n\n_${section.desc}_\n\n`;
  md += `| File | Exports |\n|---|---|\n`;
  for (const file of files) {
    const rel = relative(repoRoot, file);
    const src = readFileSync(file, 'utf8');
    const { exports, hasDefault } = extractExports(src);
    const desc = describe(src);
    if (!exports.length && !hasDefault) continue;
    const exportsStr = (hasDefault ? ['*default*', ...exports] : exports)
      .map((n) => `\`${n}\``).join(', ');
    md += `| [${rel}](${rel.replace(/^/, '../')}) | ${exportsStr || '—'} |\n`;
    totalFiles++;
    totalExports += exports.length + (hasDefault ? 1 : 0);
    if (desc) md += `| _${desc.replace(/\|/g, '\\|')}_ | |\n`;
  }
  md += `\n`;
}

md += `---\n\n`;
md += `**Stats**: ${totalFiles} files, ${totalExports} exports.\n\n`;
md += `**Last regenerated**: ${new Date().toISOString()}.\n`;

writeFileSync(outPath, md);
console.log(`✓ wrote ${relative(repoRoot, outPath)} — ${totalFiles} files, ${totalExports} exports`);
